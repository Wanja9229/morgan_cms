function _lore_esc($str) {
    return sql_real_escape_string($str);
}

function _lore_insert_article($lc_id, $title, $subtitle, $summary, $order, $sections) {
    global $g5;
    $t_article = $g5['mg_lore_article_table'];
    $t_section = $g5['mg_lore_section_table'];

    // 중복 체크
    $esc_title = _lore_esc($title);
    $chk = sql_query("SELECT la_id FROM `{$t_article}` WHERE la_title = '{$esc_title}' LIMIT 1", false);
    if ($chk && sql_num_rows($chk) > 0) return;

    $esc_subtitle = _lore_esc($subtitle);
    $esc_summary  = _lore_esc($summary);

    sql_query("INSERT INTO `{$t_article}` (lc_id, la_title, la_subtitle, la_summary, la_order, la_use, la_hit, la_created, la_updated)
               VALUES ({$lc_id}, '{$esc_title}', '{$esc_subtitle}', '{$esc_summary}', {$order}, 1, 0, NOW(), NOW())");

    $la_id = sql_insert_id();
    if (!$la_id) return;

    foreach ($sections as $i => $sec) {
        $esc_name    = _lore_esc($sec['name']);
        $esc_content = _lore_esc($sec['content']);
        $ls_order    = $i + 1;
        sql_query("INSERT INTO `{$t_section}` (la_id, ls_name, ls_type, ls_content, ls_image, ls_image_caption, ls_order)
                   VALUES ({$la_id}, '{$esc_name}', 'text', '{$esc_content}', '', '', {$ls_order})");
    }
}

